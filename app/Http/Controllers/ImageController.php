    public function store(Request $request)
    {
        if($request->hasFile('myPdfFile')){
            $myFile = $request->file('myPdfFile');
            $myFile->store('mailPdf');
        $path = Storage::putFile('mailPdf', $myFile);
        $image = new Image();
        $image->mail_id = $request->mail;
        $image->path = $path;
        $image->save();


        }
       return back();
    }

